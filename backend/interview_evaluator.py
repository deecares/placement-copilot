def detect_category(question: str) -> str:
    """
    Heuristically classifies the question text into one of 5 categories:
    HR, Technical, Coding, System Design, or Project Discussion.
    """
    q_lower = question.lower()
    
    # 1. Project Discussion checks
    if any(phrase in q_lower for phrase in ["proud of", "your project", "previous project", "describe a project", "tell me about a project", "project you built"]):
        return "Project Discussion"
        
    # 2. HR / Behavioral checks
    if any(phrase in q_lower for phrase in ["tell me about yourself", "why do you want", "disagree", "deadline", "conflict", "weakness", "strength", "leadership", "teamwork", "handle a situation", "describe a time", "how do you handle"]):
        return "HR"
        
    # 3. Coding checks
    if any(phrase in q_lower for phrase in ["write a", "implement a", "implement an algorithm", "solve the", "coding challenge", "program to", "function to", "sql query", "decorator", "palindrome", "fibonacci"]):
        return "Coding"
        
    # 4. System Design checks
    if any(phrase in q_lower for phrase in ["design a", "design the", "system design", "load balancer", "scaling", "architecture for", "cdn", "kafka", "twilio", "cap theorem", "slack", "twitter"]):
        return "System Design"
        
    # 5. Default to Technical
    return "Technical"

def evaluate_hr(question: str, answer: str) -> dict:
    """
    HR Rubric: Communication, STAR structure, Confidence, Leadership, Teamwork
    """
    ans_lower = answer.lower()
    strengths = []
    weaknesses = []
    missing_concepts = []
    
    # 1. Communication (transition words check)
    struct_words = ["because", "therefore", "first", "second", "however", "consequently"]
    comm_score = min(2, sum(1 for w in struct_words if w in ans_lower))
    if comm_score >= 2:
        strengths.append("Structured communication style with smooth transitions.")
    else:
        weaknesses.append("Try organizing your points with transitions (e.g. 'firstly', 'secondly').")
        
    # 2. STAR Structure
    star_words = ["situation", "task", "action", "result", "consequently", "resolved", "solved"]
    has_star = sum(1 for w in star_words if w in ans_lower)
    star_score = 2 if has_star >= 2 else 1 if has_star >= 1 else 0
    if star_score == 2:
        strengths.append("Followed the STAR (Situation, Task, Action, Result) method framework.")
    else:
        weaknesses.append("Ensure your story explicitly defines the Situation, Task, Action, and Result (STAR method).")
        missing_concepts.append("STAR storytelling structure")
        
    # 3. Confidence (absence of hesitant words)
    hesitant_words = ["maybe", "guess", "probably", "i think", "sort of", "kind of"]
    has_hesitant = any(w in ans_lower for w in hesitant_words)
    confidence_score = 1 if has_hesitant else 2
    if confidence_score == 2:
        strengths.append("Assertive tone with confident word choices.")
    else:
        weaknesses.append("Avoid hesitant phrases like 'I think' or 'maybe' to project more authority.")
        
    # 4. Leadership
    lead_words = ["led", "guided", "spearheaded", "directed", "mentored", "initiated", "responsible"]
    has_lead = any(w in ans_lower for w in lead_words)
    lead_score = 2 if has_lead else 0
    if lead_score == 2:
        strengths.append("Highlighted proactive leadership and ownership.")
    else:
        weaknesses.append("Incorporate examples of taking direct initiative or leading a task.")
        missing_concepts.append("Leadership & Initiative indicators")
        
    # 5. Teamwork
    team_words = ["team", "collaborated", "together", "we", "shared", "partnered", "supported"]
    has_team = any(w in ans_lower for w in team_words)
    team_score = 2 if has_team else 0
    if team_score == 2:
        strengths.append("Communicated strong collaboration and teamwork alignment.")
    else:
        weaknesses.append("Balance your achievements by mentioning team syncs and support.")
        missing_concepts.append("Team collaboration keywords")
        
    score = comm_score + star_score + confidence_score + lead_score + team_score
    score = min(10, max(1, score))
    
    ideal = "An ideal behavioral response starts with the Situation (10%), defines the Task and constraints (15%), details your specific Action (60%), and finishes with a quantifiable positive Result (15%)."
    follow_up = "What was the direct impact of your actions on the team's timeline or final deliverable?"
    
    return {
        "score": score,
        "strengths": strengths,
        "weaknesses": weaknesses,
        "missingConcepts": missing_concepts,
        "idealAnswer": ideal,
        "followUpQuestion": follow_up
    }

def evaluate_technical(question: str, answer: str, role: str) -> dict:
    """
    Technical Rubric: Accuracy, Technical depth, Terminology, Practical understanding
    """
    ans_lower = answer.lower(); q_lower = question.lower()
    strengths = []
    weaknesses = []
    missing_concepts = []
    
    # Identify Closest Question in Database
    match_info = None
    for key, info in QUESTION_EVALUATION_DB.items():
        if key in q_lower:
            match_info = info
            break
            
    # 1. Technical Depth (length score)
    words = [w for w in answer.split() if w]
    depth_score = min(3, len(words) // 15) # up to 3 points
    if depth_score >= 2:
        strengths.append("Response shows good technical depth and explanation length.")
    else:
        weaknesses.append("Elaborate further on internal mechanics; response is too brief.")
        
    # 2 & 3. Accuracy and Terminology (up to 5 points)
    accuracy_score = 0
    if match_info:
        target_kws = match_info["keywords"]
        matched_kws = [kw for kw in target_kws if re.search(rf"\b{re.escape(kw)}\b", ans_lower)]
        unmatched_kws = [kw for kw in target_kws if kw not in matched_kws]
        
        ratio = len(matched_kws) / len(target_kws)
        accuracy_score = int(ratio * 5)
        
        if len(matched_kws) > 0:
            strengths.append(f"Accurate usage of primary terms: {', '.join(matched_kws[:3])}.")
        if len(unmatched_kws) > 0:
            weaknesses.append(f"Incorporate missing core terms like: {', '.join(unmatched_kws[:2])}.")
            
        concepts = match_info["concepts"]
        for idx, concept in enumerate(concepts):
            kw_index = min(idx, len(target_kws) - 1)
            if target_kws[kw_index] not in matched_kws:
                missing_concepts.append(concept)
                
        ideal = match_info["ideal"]
        follow_up = match_info["follow_up"]
    else:
        # Fallback role-based keywords check
        fallback_map = {
            "game developer": ["unity", "unreal", "c#", "c++", "physics", "rendering", "oop"],
            "frontend developer": ["react", "javascript", "html", "css", "state", "dom"],
            "backend developer": ["python", "api", "database", "sql", "server", "docker"],
            "ai/ml engineer": ["python", "model", "learning", "tensorflow", "pytorch", "data"],
            "software engineer": ["code", "design", "oop", "algorithm", "git", "data"]
        }
        kws = fallback_map.get(role.lower(), ["architecture", "systems", "implementation"])
        matched_kws = [kw for kw in kws if kw in ans_lower]
        accuracy_score = min(5, len(matched_kws) * 1)
        
        if len(matched_kws) > 0:
            strengths.append(f"Mentioned core role domain terms: {', '.join(matched_kws[:2])}.")
        else:
            weaknesses.append("Align terminology with core software engineering concepts.")
            missing_concepts.append("Domain-specific architectural keywords")
            
        ideal = "Define the core technical mechanism, describe how it interacts with other modules, and outline its performance trade-offs."
        follow_up = "How does this scale or bottleneck under high load conditions?"
        
    # 4. Practical Understanding (check for examples / projects)
    practical_words = ["example", "project", "production", "use case", "experience", "applied", "work"]
    has_practical = any(w in ans_lower for w in practical_words)
    practical_score = 2 if has_practical else 0
    if practical_score == 2:
        strengths.append("Linked theoretical concept with practical application/projects.")
    else:
        weaknesses.append("Provide a short real-world scenario where you utilized this concept.")
        missing_concepts.append("Production application context")
        
    score = depth_score + accuracy_score + practical_score
    score = min(10, max(1, score))
    
    return {
        "score": score,
        "strengths": strengths,
        "weaknesses": weaknesses,
        "missingConcepts": missing_concepts,
        "idealAnswer": ideal,
        "followUpQuestion": follow_up
    }

def evaluate_coding(question: str, answer: str) -> dict:
    """
    Coding Rubric: Algorithm, Complexity, Edge cases, Code quality
    """
    ans_lower = answer.lower()
    strengths = []
    weaknesses = []
    missing_concepts = []
    
    # 1. Algorithm implementation structure
    algo_words = ["loop", "recursion", "array", "pointer", "time", "space", "tree", "hash", "list", "stack", "queue"]
    has_algo = sum(1 for w in algo_words if w in ans_lower)
    algo_score = min(3, has_algo // 2 + 1)
    if algo_score >= 2:
        strengths.append("Understands the core data structure or algorithmic pattern.")
    else:
        weaknesses.append("Focus on details of the exact sorting, traversing, or mapping algorithm.")
        
    # 2. Complexity Analysis
    complexity_patterns = [r"\bo\(", r"complexity", r"time complexity", r"space complexity", r"big o", r"runtime"]
    has_complexity = any(re.search(pat, ans_lower) for pat in complexity_patterns)
    complexity_score = 2 if has_complexity else 0
    if complexity_score == 2:
        strengths.append("Explicitly discussed space/time complexity bounds.")
    else:
        weaknesses.append("Missing Big O notation; state the space and time complexity explicitly.")
        missing_concepts.append("Big O Complexity Analysis")
        
    # 3. Edge Cases
    edge_words = ["edge", "null", "empty", "boundary", "zero", "negative", "overflow", "exception", "error", "bounds"]
    has_edge = any(w in ans_lower for w in edge_words)
    edge_score = 2 if has_edge else 0
    if edge_score == 2:
        strengths.append("Considered boundaries, errors, and input edge cases.")
    else:
        weaknesses.append("Explain how your code behaves with empty, null, or extreme boundary inputs.")
        missing_concepts.append("Input Validation & Edge Case Check")
        
    # 4. Code Quality / Syntax structure
    syntax_words = ["def ", "return", "class", "function", "let ", "const ", "if ", "while", "for ", "{", "}", "import"]
    has_syntax = sum(1 for w in syntax_words if w in ans_lower)
    syntax_score = min(3, has_syntax)
    if syntax_score >= 2:
        strengths.append("Structured answers with valid code syntax or clear pseudo-code blocks.")
    else:
        weaknesses.append("Use proper indentation and code structures rather than raw paragraphs.")
        missing_concepts.append("Structured Code Syntax / Pseudo-code")
        
    score = algo_score + complexity_score + edge_score + syntax_score
    score = min(10, max(1, score))
    
    ideal = "Write a clean function definition, validate inputs for edge cases (null/empty), implement the algorithm using efficient data structures, write comments, and state the exact time complexity (e.g. O(N)) and space complexity (e.g. O(1))."
    follow_up = "What is the worst-case space complexity of your solution and can it be optimized?"
    
    return {
        "score": score,
        "strengths": strengths,
        "weaknesses": weaknesses,
        "missingConcepts": missing_concepts,
        "idealAnswer": ideal,
        "followUpQuestion": follow_up
    }

