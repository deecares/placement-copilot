def evaluate_interview_answer(question: str, answer: str, role: str) -> dict:
    """
    Classifies the interview question, runs category-specific scoring evaluation,
    and returns a cohesive feedback payload.
    """
    category = detect_category(question)
    
    # 1. Base check for blank/brief answers
    words = [w for w in answer.strip().split() if w]
    if len(words) < 5:
        return {
            "score": 1,
            "strengths": ["Recognized the request and initiated a reply."],
            "weaknesses": ["Answer is too brief to evaluate.", "Fails to provide sufficient technical depth."],
            "missingConcepts": ["Fundamental definitions", "Technical terminology"],
            "idealAnswer": "Provide a complete definition, trace implementation steps, and mention projects or metrics.",
            "followUpQuestion": "Could you try explaining this topic with a concrete example?",
            "confidenceLevel": "Low"
        }
        
    # 2. Route evaluation to category-specific scoring
    if category == "HR":
        eval_result = evaluate_hr(question, answer)
    elif category == "Coding":
        eval_result = evaluate_coding(question, answer)
    elif category == "System Design":
        eval_result = evaluate_system_design(question, answer)
    elif category == "Project Discussion":
        eval_result = evaluate_project_discussion(question, answer)
    else:
        eval_result = evaluate_technical(question, answer, role)
        
    # 3. Add Confidence Level based on score
    score = eval_result["score"]
    if score >= 8:
        confidence = "High"
    elif score >= 5:
        confidence = "Medium"
    else:
        confidence = "Low"
        
    eval_result["confidenceLevel"] = confidence
    
    return eval_result
